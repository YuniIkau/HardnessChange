	//リソースの初期化
	bool  Resource::Initialize()
	{
		return true;
	}
	//-------------------------------------------------------------------
	//リソースの解放
	bool  Resource::Finalize()
	{
		return true;
	}
	//-------------------------------------------------------------------
	//「初期化」タスク生成時に１回だけ行う処理
	bool  Object::Initialize()
	{
		//スーパークラス初期化
		__super::Initialize(defGroupName, defName, true);
		//リソースクラス生成orリソース共有
		this->res = Resource::Create();

		//★データ初期化
		this->render2D_Priority[1] = 0.1f;
		this->pos.y = (float)ge->screenHeight / 3;
		//★タスクの生成

		return  true;
	}
	//-------------------------------------------------------------------
	//「終了」タスク消滅時に１回だけ行う処理
	bool  Object::Finalize()
	{
		return  true;
	}
	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		if (this->pos.x <= ge->screenWidth / 2 - 240)
		{
			this->pos.x = (float)ge->screenWidth / 2 - 240;
			this->uiState = 決定;
		}
		else 
		{
			this->pos.x -= 9;
		}
	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Box2D drawT((int)this->pos.x, (int)this->pos.y, 480, 120);
		//それぞれの状態に応じたリザルト背景の表示
		if (ge->gameState == ge->クリア || ge->gameState == ge->オールクリア)
		{
			ML::Box2D srcT(0, 150, 800, 150);
			DG::Image_Draw("StageLogoImg", drawT, srcT);
		}
		else
		{
			ML::Box2D srcT(0, 0, 800, 150);
			DG::Image_Draw("StageLogoImg", drawT, srcT);
		}
	}

