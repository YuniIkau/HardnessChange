	//リソースの初期化
	bool  Resource::Initialize()
	{
		//シングルトン
		if (!ge->GetTask_One_GN<ResourceManager::Object>("リソースマネージャー", "リソースマネージャー"))
		{
			//リソースマネージャーの生成
			auto RM = ResourceManager::Object::Create(true);
		}
		auto RM = ge->GetTask_One_GN<ResourceManager::Object>("リソースマネージャー", "リソースマネージャー");
		//タイトルシーンで使用するリソースの読み込み
		RM->Create(RM->Title);
		//BGMの再生開始
		DM::Sound_Play("Title", true);
		return true;
	}
	//-------------------------------------------------------------------
	//リソースの解放
	bool  Resource::Finalize()
	{
		auto RM = ge->GetTask_One_GN<ResourceManager::Object>("リソースマネージャー", "リソースマネージャー");
		RM->Erase(RM->Title);
		return true;
	}
