	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		auto	cm = ge->GetTask_One_GN<Game_Camera::Object>("ゲーム", "カメラ");
		ML::Box2D draw1 = ML::Box2D(0, 0, ge->screenWidth, ge->screenHeight);
		ML::Box2D src1(0, 0, ge->screenWidth, ge->screenHeight);

		//カメラの矩形の中をカラーの画像に差し替える
		ML::Box2D draw2 = ML::Box2D((int)cm->pos.x, (int)cm->pos.y, 300, 320);
		ML::Box2D src2((int)(cm->pos.x ), (int)(cm->pos.y ), (int)(300 ), (int)(320 ));

		DG::Image_Draw("BackMonoImg", draw1, src1);
		DG::Image_Draw("BackImg", draw2, src2);
	}

